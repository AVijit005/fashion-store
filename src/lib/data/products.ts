export type Product = {
  id: string;
  slug: string;
  name: string;
  category: string;
  tagline: string;
  price: number;
  mrp: number;
  rating: number;
  reviews: number;
  colors: { name: string; hex: string }[];
  sizes: string[];
  images: string[];
  badges: ("new" | "trending" | "limited" | "oversized" | "anime" | "bestseller")[];
  story: string;
};

const u = (id: string, w = 900) =>
  `https://images.unsplash.com/${id}?w=${w}&q=80&auto=format&fit=crop`;

const SIZES = ["XS", "S", "M", "L", "XL", "XXL"];
const COLORS = [
  { name: "Bone", hex: "#f5f3ee" },
  { name: "Fog", hex: "#e8e4dd" },
  { name: "Graphite", hex: "#2d2d2d" },
  { name: "Ink", hex: "#0d0d0d" },
  { name: "Ember", hex: "#c84b1e" },
  { name: "Forest", hex: "#2f4a3a" },
];

const make = (
  i: number,
  slug: string,
  name: string,
  category: string,
  tagline: string,
  price: number,
  mrp: number,
  imgIds: string[],
  badges: Product["badges"],
  story: string,
): Product => ({
  id: `p-${i}`,
  slug,
  name,
  category,
  tagline,
  price,
  mrp,
  rating: 4 + (i % 10) / 10,
  reviews: 120 + i * 37,
  colors: COLORS.slice(0, 4 + (i % 3)),
  sizes: SIZES,
  images: imgIds.map((id) => u(id, 1200)),
  badges,
  story,
});

